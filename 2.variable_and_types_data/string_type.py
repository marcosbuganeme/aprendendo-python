"""
Type string is a sequence of characters. It is a data type that is used to store text rather than numbers.
"""

name = 'Marcos \nBuganeme'
print(name)
print(type(name))

name = "Can'abis for sales"
print(name)
print(type(name))

name = 'Marcos Buganeme'
print(name)
print(type(name))

print(name.upper())
print(name.lower())
print(name.split())
print(name.capitalize())
print(name.__len__())
print(name.swapcase())
print(name[::-1])
